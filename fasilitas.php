            <p>Masjid di Dusun  Sumur bernama Masjid Al- Hikmah. Masjid ini didirikan tahun 1995. Masjid  tersebut digunakan untuk tempat ibadah warga Dusun Sumur karena sebagian besar  warga Dusun Sumur beragama muslim dan dijadikan sebagai tempat untuk TPA  anak-anak di Dusun Sumur. Masjid Al-Hikmah memiliki berbagai fasilitas  diantaranya sajadah, lemari yang berisi Al-Qur'an, Iqra, serta buku-buku bacaan  islami, tempat wudhu, kamar mandi, mukena, mimbar, kipas angin dan lain-lain.  Masjid ini juga digunakan sebagai pusat keagamaan contohnya untuk sholat 5  waktu, sholat jum'at, Taman Pendidikan Al-Qur&rsquo;an (TPA).<strong> </strong></p>
