        <td><h3 class="style3">TK</h3>
          <p align="justify">Di Dusun Sumur  terdapat satu fasilitas Pendidikan yaitu TK Aisiyyah Bustanul Athfal VII. Letak  TK ini tepat di depan Masjid Al-Hikmah. Murid di TK Aisiyyah Bustanul Athfal  VII tidak hanya anak-anak dari dusun sumur saja, tetapi dari dusun lain juga  banyak yang bersekolah di tempat ini. Di dalam TK ini terdapat berbagai  fasilitas penunjang yang mendukung dalam proses belajar megajar. Terdapat  fasilitas bermain juga seperti ayunan, jungkat jungkit dan lain lain.</p>          
