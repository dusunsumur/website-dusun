        <td><h3 class="style3">Lapangan Voli </h3>
          <p align="justify">Lapangan ini  terletak di sebelah Masjid Al-Hikmah Dusun Sumur, menempati wilayah yang cukup  luas. Lapangan tersebut umumnya difungsikan sebagai tempat bermain voli bagi  pemuda-pemuda warga Sumur. Tradisi bermain bola voli di lapangan ini secara  rutin dilaksanakan pada hari Minggu, menciptakan suasana kebersamaan dan  kegiatan rekreasi yang positif bagi masyarakat setempat. Keberadaan lapangan  ini tidak hanya menjadi tempat untuk berolahraga, tetapi juga sebagai sarana  mempererat tali persaudaraan dan meningkatkan kesehatan serta kebugaran fisik  bagi para pemain dan penonton yang turut serta dalam kegiatan tersebut. Namun  sayangnya lapangan voli tersebut sudah lama tidak dipakai lagi karna warga  dusun sumur sebagian merantau ke luar kota untuk bekerja.</p>        </td>
